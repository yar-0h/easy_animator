package cs5004.animator.model;

/**
 * This enumerated type represents the 3 current shapes upported by the EasyAnimatorModel.
 * POLYGON a shape with multiple vertices, from triangles to triskaidecagons and beyond (ideally).
 * RECTANGLE simple rectangle of variable height and width.
 * ELLIPSE, a simple ellipse of variable height and width".
 */

public enum ShapeType {
    POLYGON, RECTANGLE, ELLIPSE
}
